					<h3 class="title">A few things about me</h3>
					<p>I'm from <a href="http://en.wikipedia.org/wiki/Chile" target="_blank">Chile</a>. I have a B.Sc. in Engineering Mathematics and a Mathematical Engineering professional degree (which is kind of equivalent to a Ms.Sc. in Mathematics) from the <a href="http://www.dim.uchile.cl/" target="_blank">Departamento de Ingeniería Matemática</a> at the <a href="http://www.uchile.cl" id="University" name="University" target="_blank">Universidad de Chile</a>, where I worked under the supervision of <a href="http://capde.cl/juan-davila/" target="_blank">Juan Dávila</a> on a singular perturbation problem. The thesis (in spanish) and the article that I wrote about the subject can be found in my publication list.</p>
	
					<p>I completed my Ph.D. in Mathematics studies at the <a href="http://www.math.rutgers.edu/" target="_blank">Mathematics Department</a> from <a href="http://www.rutgers.edu/" target="_blank">Rutgers University</a> with <a href="http://www.math.rutgers.edu/~brezis/" target="_blank">Haim Brezis</a> as my advisor. My thesis is the compilation of the research I did while at Rutgers about singular Sturm-Liouville equations. You can find the thesis and the respective papers in my publication list.</p>
	
                    <p>Currently I am an Assistant Professor at the <a href="http://inst-mat.utalca.cl/" target="_blank">Instituto de Matemática y Física</a> from <a href="http://www.utalca.cl/" target="_blank">Universidad de Talca</a> where I teach both undergraduate and graduate courses. You can see my current reseach interests by clicking the <i>Research</i> and <i>Publications</i> links from the side panel.</p>

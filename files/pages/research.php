							<div class="details">
								<p>During the course of my Ph.D. studies, I developed a theory about the singular second order Sturm-Liouville operator \(L_\alpha u(x)=-(x^{2\alpha}u'(t))'\) on the interval \((0,1)\), where \(\alpha>0\) is a real parameter. One of the main reasons I studied this operator is that it serves as a toy model to more general degenerate elliptic operators, and results obtained in this simpler setup might shed some light into the study of more general cases.</p>

								<p>In joints works with H. Wang we used tools from functional analysis to prove existence and regularity, uniqueness of solutions, as-well as spectral properties of the equation \(L_\alpha u+u=f\). The core idea behind this study was to look for solutions in appropriate weighted Sobolev spaces (which had to be constructed for the occasion). Later, I studied the semi-linear eigenvalue problem \(L_\alpha u=\lambda u+|u|^{p-1}u\), where \(\lambda\) and \(p>1\) are real parameters. In those works, I performed a bifurcation analysis for \(L_\alpha\), determining the relationship between the parameters \(\alpha,~\lambda\) and \(p\) and the existence/non-existence of a branch of positive solutions emanating form the first eigenvalue \(\lambda_1\) of \(L_\alpha\).</p>

								<p>The main goal of this work will be to continue the research about the operator \(L_\alpha\) by answering some of the open problems that arose in the works cited before.</p>

								<p>In terms of the semi-linear eigenvalue problem \(L_\alpha u=\lambda u+|u|^{p-1}u\) the open problems I plan to attack can be divided into two cases with respect to the parameter \(\alpha\): \(0 < \alpha<1\) and \(\alpha\geq 1\). In the case where \(0<\alpha<1\) there are a few open questions, specially when the parameter \(p\) is large: in a previous article we showed that positive solutions only exist in a range \(\hat\lambda<\lambda<\lambda_1\) for <em>some</em> \(\hat\lambda>0\), however we still do not have a precise description of the <em>optimal</em> \(\hat\lambda\), and how the branch of positive solutions behave near that \(\hat\lambda\). When \(\alpha\geq 1\), we know that all solutions satisfying \(u(1)=0\) must have infinite sing changes in the interval \((0,1)\), however, we would like to know how fast the sing changes occur as we approach \(x=0\); also, the existence of bounded solutions in this case remain an open question.</p>

								<p>On the other hand, the operator \(L_\alpha u(x)=-(x^{2\alpha}u'(x))'\) can be generalized into two different directions:
									<ul>
										<li>We can consider the operator \(L_A u(x):=-(A(x)u'(x))'\), where \(A(x)\) is a non-negative function that <em>looks</em> like \(x^{2\alpha}\) near \(x=0\), and try to study the spectral properties of such operator. The main problem I plan to address occurs when \(\alpha=1\): we would like to describe the spectrum of the operator \(\mathcal L_A\), in particular, finding conditions on \(A(x)\) for the existence/non-existence of eigenvalues has proven to be quite challenging (see the works of Stuart and Villaume for the case \(\alpha=1\)).</li>

										<li>We can consider an elliptic operator in \(N+1\) dimensions of the form \(L_A u(x,y):=-\mathrm{div}(A(y)\nabla u(x,y))\), where \((x,y)\in \Omega\times(0,M)\), \(\Omega\subset \mathbb{R}^N\), \(0<M \leq \infty \), and \(A(y)\) is a non-negative function that looks like \(y^{2\alpha}\). In particular, when \(\Omega=\mathbb{R}^N,~M=+\infty\), and \(A(y)=y^{1-2s}\) for \(s\in (0,1)\), the operator is closely related to the fractional Laplacian \((-\Delta)^s\), who in turn appears in several real life applications ranging from physics to economics. Also, we would like to have a good understanding of the kind of boundary conditions that can be imposed at \(y=0\) for equations of the type \(L_A u=f(x,y,u)\), as this will be of great help when working with similar elliptic operators in domains in \(\mathbb{R}^{N+1}\).</li>
									</ul>

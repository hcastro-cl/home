										<h3>Singular Sturm-Liouville equations</h3>
										<p>A study of equations involving the differential operator \(L_A(u)(x)=-(A(x)u'(x))'\) for \(A(x)\sim x^{2\alpha}\) and \(\alpha>0\).</p>

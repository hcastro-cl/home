									<div class="meta">
										<h3>Hardy-Sobolev inequalities</h3>
										<p>Extremals and least energy solutions associated to a family of Hardy-Sobolev inequalities.</p>
									</div>
								</div>
							</div>
							<div class="details">
								<p>In the past years I have been studying singular (degenerate) second order elliptic operators such as the singular Sturm-Liouville operator \(L_\alpha u(t):=-(t^{2\alpha}u'(t))'\) where \(\alpha>0\), or the degenerate elliptic operator \(\mathcal L_\alpha u(x):=-\mathrm{div}(x^A\nabla u(x))\) where \(A\in\mathbb R^N\) is a given vector and \(x^A=|x_1|^{a_1}\cdot\ldots |x_N|^{a_N}\). In such studies the construction of suitable weighted Sobolev spaces and the study of their properties were quite significant and opened a very interesting line of research. In particular, it was established the validity of the following Hardy-Sobolev inequalities
\begin{equation}\label{hs-ineq}
\left(\int_{\mathbb R^N} \left|x^B u(x)\right|^{p^*} dx\right)^{\frac 1{p^*}}\leq C\left(\int_{\mathbb R^N}\left|x^A\nabla u(x)\right|^p dx\right)^{\frac1p}
\end{equation}
for suitable \(p\geq 1\), \(A,B\in\mathbb R^N\) and 
\[
p^*=\frac{Np}{N-p(1+b-a)},
\]
where \(a=a_1+\ldots+a_N\) and \(b=b_1+\ldots+b_N\). This work leads to several interesting open problems, some of which are the purpose of this research project.</p>

                                <p>One of the natural topics of interest that arise when one has proved such an inequality is the existence of extremals and the value of the best possible constant \(C>0\). Namely, for a given open set \(\Omega\subseteq\mathbb R^N\), we would like to investigate the existence of a function \(u_{A,B,p}=u_p\) and the value of the constant \(S_{A,B,p}>0\) such that 
\[
S_{A,B,p}^p(\Omega)=\inf\left\{\int_{\Omega}\left|x^A\nabla u(x)\right|^p dx:~u\in D^1_{A,p}(\Omega),~\int_{\mathbb R^N} \left|x^B u(x)\right|^{p^*} dx=1\right\}=\int_{\Omega}\left|x^A\nabla u_p(x)\right|^p dx,
\]
here \(D^1_{A,p}(\Omega)\) denotes the completion of \(C^\infty_0(\Omega)\) under the norm \(\Vert u\Vert^p=\int_{\Omega}\left|x^A\nabla u(x)\right|^p dx\). The main difficulty in this problem arises when one notices that \(p^*\) is critical from the viewpoint of the embedding of the weighted Sobolev space into the weighted \(L^q\) space. More precisely, if \(\Omega\) is bounded and contains the origin then the space \(D^1_{A,p}(\Omega)\) is embedded into the respective weighted space \(L^q(\Omega, x^{Bp^*})\) for \(1\leq q\leq p^*\), but the embedding is compact if and only if \(q<{p^*}\). This lack of compactness prevents us from using the classical minimization strategy to obtain extremals for \(S_{A,B,p}(\Omega)\), thus making this a problem worth addressing.</p>

<p>The other problem we plan to attack in this research project concerns the the asymptotic behavior of minimizer sequences for the problem
\[
S^p_{A,p}=\inf\left\{\int_{\Omega}\left|\nabla u(x)\right|^2x^A dx:u\in D^1_{A,2}(\Omega),~\int_{\Omega} \left|u(x)\right|^{p}x^A dx=1\right\}
\]
where \(p\nearrow 2^*\). More precisely, since the embedding from \(D^1_{A,2}(\Omega)\) to \(L^p(\Omega,x^Adx)\) is compact for \(q<{2^*}\) we can assert the existence of a function \(u_{A,p}=u_p\in D^1_{A,2}(\Omega)\) such that \(\int_{\Omega} \left|u(x)\right|^{p}x^A dx=1\) and
\[
\int_{\Omega}\left|\nabla u_p(x)\right|^2x^A dx=S^2_{A,p}+o(1)
\]
as \(p\nearrow 2^*\). The idea is to obtain a generalization of the known results about the classical Sobolev inequality (\(A=0\)). For example, we would like to know if \(u_p\) develops blow up points as \(p\nearrow 2^*\), and if this is the case, we would also like to discover the location of such blow up points and the respective limiting profile of suitable normalized minimizers.</p>
